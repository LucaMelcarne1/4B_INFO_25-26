public class Pilota {
    String nome;
    String cognome;
    int anno_nascita;
    boolean patente;

    public Pilota() {
    }
    
    public Pilota(String nome, String cognome, int anno_nascita, boolean patente) {
        this.nome = nome;
        this.cognome = cognome;
        this.anno_nascita = anno_nascita;
        this.patente = patente;
    }
    
}
